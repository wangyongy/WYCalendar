                          @"寒露", @"霜降", @"立冬", @"小雪", @"大雪", @"冬至", nil];
    
    NSInteger index = (components.year - startYear)*12 + components.month - 1 ;
    
    NSInteger flag = solarTermsDay[index];
    
    NSInteger day;
    
    if(components.day <15) day = 15 - ( (flag>>4) & 0x0f);
    
    else day = ((flag)&0x0f)+15;
    
    NSInteger result = -1;
    
    if(components.day == day){
        
        result = (components.month - 1) * 2 + (components.day > 15 ? 1 : 0);
    }
    
    if ( result >= 0  && result < [chineseDays count] ) {
        
