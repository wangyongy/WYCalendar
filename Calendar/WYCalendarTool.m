/**
 农历节日

 @return 返回当前日期对应的农历节日
 */
- (NSString *)getLunarHoliDayDate
{
    
    NSDate * nextDayDate = [NSDate dateWithTimeInterval:60*60*24 sinceDate:self];
    
    NSCalendar *localeCalendar = [[NSCalendar alloc] initWithCalendarIdentifier:NSCalendarIdentifierChinese];
    
    NSInteger unitFlags = NSCalendarUnitYear | NSCalendarUnitMonth |  NSCalendarUnitDay;
    
    NSDateComponents *components = [localeCalendar components:unitFlags fromDate:nextDayDate];
    
    //春节前一天是除夕（具体日期不确定）
    if ( 1 == components.month && 1 == components.day ) {
        
        return @"除夕";
    }
    
    components = [localeCalendar components:unitFlags fromDate:self];
    
    NSDictionary *chineseHoliDay = [NSDictionary dictionaryWithObjectsAndKeys:
                                    
                                    @"春节", @"1-1",
                                    
                                    @"元宵", @"1-15",
                                    
                                    @"端午", @"5-5",
                                    
                                    @"七夕", @"7-7",
                                    
                                    @"中元", @"7-15",
                                    
                                    @"中秋", @"8-15",
                                    
                                    @"重阳", @"9-9",
                                    
                                    @"腊八", @"12-8",
                                    
                                    @"小年", @"12-24",
                                    
                                    nil];
    
    NSString * result = [chineseHoliDay objectForKey:[NSString stringWithFormat:@"%zd-%zd",components.month,components.day]];
    
    if (result == nil) result = [self getSolarTermDate:[[NSCalendar currentCalendar] components:unitFlags fromDate:self]];
    
    if (result == nil) result = [self getChineseDate:components];
    
    return result;
}

